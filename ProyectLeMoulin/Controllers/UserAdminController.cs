        {
            CoeurContainer db = new CoeurContainer();

            AspNetUser s = (from r in db.AspNetUsers where r.Id == id select r).SingleOrDefault();

            s.Suspendre = true;

            db.SaveChanges();

            string result = "ici le result";

            return Json(result);
        }
    }
}
